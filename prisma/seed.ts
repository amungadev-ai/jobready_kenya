// ============================================================
// MAIN SEED FUNCTION
// ============================================================

export async function seed() {
  const prisma = new PrismaClient({
    datasourceUrl: DATABASE_URL,
    log: ['error', 'warn'],
  });

  try {
    console.log('🌱 Starting database seed...\n');

    // -------------------------------------------------------
    // 0. CLEAN ALL TABLES (idempotent re-seed)
    // -------------------------------------------------------
    console.log('🧹 Cleaning all tables...');
    await prisma.job.deleteMany({});
    await prisma.opportunity.deleteMany({});
    await prisma.jobSubcategory.deleteMany({});
    await prisma.jobCategory.deleteMany({});
    await prisma.organization.deleteMany({});
    console.log('   ✅ All tables cleared.\n');

    // -------------------------------------------------------
    // 1. SEED CATEGORIES + SUBCATEGORIES
    // -------------------------------------------------------
    console.log('📂 Seeding categories and subcategories...');

    // Detect duplicate subcategory values across categories for disambiguation
    const subValueToCats = new Map<string, string[]>();
    const subSlugToCats = new Map<string, string[]>();
    for (const cat of jobCategoryData) {
      for (const sub of cat.subcategories) {
        const list = subValueToCats.get(sub.value) || [];
        list.push(cat.value);
        subValueToCats.set(sub.value, list);

        const s = slugify(sub.label);
        const slist = subSlugToCats.get(s) || [];
        slist.push(cat.value);
        subSlugToCats.set(s, slist);
      }
    }

    // Create categories (need IDs for subcategories)
    const createdCategories: { id: string; value: string }[] = [];
    for (let i = 0; i < jobCategoryData.length; i++) {
      const cat = jobCategoryData[i];
      const created = await prisma.jobCategory.create({
        data: {
          value: cat.value,
          label: cat.label,
          slug: slugify(cat.label),
          description: cat.description,
          sortOrder: i,
        },
      });
      createdCategories.push({ id: created.id, value: cat.value });
    }

    // Batch-create subcategories using createMany
    let totalSubs = 0;
    for (let i = 0; i < jobCategoryData.length; i++) {
      const cat = jobCategoryData[i];
      const catId = createdCategories[i].id;

      const subData = cat.subcategories.map((sub, j) => {
        const catsUsing = subValueToCats.get(sub.value) || [];
        const subValue = catsUsing.length > 1
          ? `${sub.value}_${cat.value}`
          : sub.value;
        const subSlugBase = slugify(sub.label);
        const slugCats = subSlugToCats.get(subSlugBase) || [];
        const subSlug = slugCats.length > 1
          ? `${subSlugBase}-${cat.value.toLowerCase()}`
          : subSlugBase;
        return {
          value: subValue,
          label: sub.label,
          slug: subSlug,
          categoryId: catId,
          sortOrder: j,
        };
      });

      if (subData.length > 0) {
        const result = await prisma.jobSubcategory.createMany({ data: subData });
        totalSubs += result.count;
      }
    }

    console.log(`   ✅ Created ${createdCategories.length} categories and ${totalSubs} subcategories.`);

    // -------------------------------------------------------
    // 2. SEED ORGANIZATIONS
    // -------------------------------------------------------
    console.log('\n🏢 Seeding organizations...');

    const orgResult = await prisma.organization.createMany({
      data: organizations.map(org => ({
        orgName: org.name,
        orgSlug: org.orgSlug,
        orgIndustry: org.orgIndustry as any,
        orgType: org.orgType as any,
        orgDescription: org.description,
        orgWebsite: org.website,
        headquarters: `${org.city}, ${org.county}`,
        isVerified: true,
        isActive: true,
      })),
    });
    console.log(`   ✅ Created ${orgResult.count} organizations.`);

    // -------------------------------------------------------
    // 3. SEED JOBS
    // -------------------------------------------------------
    console.log('\n💼 Seeding jobs...');

    // Build lookup maps
    const allSubcategories = await prisma.jobSubcategory.findMany({
      select: { id: true, value: true, categoryId: true },
    });

    const subcategoryMap = new Map<string, { id: string; categoryId: string }>();
    for (const sub of allSubcategories) {
      subcategoryMap.set(sub.value, { id: sub.id, categoryId: sub.categoryId });
    }

    // Also build a map for disambiguated subcategory values
    // If a job references "PROGRAM_MANAGEMENT_NGO" and the stored value is "PROGRAM_MANAGEMENT_NGO", use it directly
    // If not found, try without disambiguation suffix
    const subValueToCats2 = new Map<string, string[]>();
    for (const cat of jobCategoryData) {
      for (const sub of cat.subcategories) {
        const list = subValueToCats2.get(sub.value) || [];
        list.push(cat.value);
        subValueToCats2.set(sub.value, list);
      }
    }

    function resolveSubcategoryValue(rawValue: string, categoryValue: string): string | undefined {
      // Direct lookup
      if (subcategoryMap.has(rawValue)) return rawValue;
      // Try disambiguated value
      const disambiguated = `${rawValue}_${categoryValue}`;
      if (subcategoryMap.has(disambiguated)) return disambiguated;
      return undefined;
    }

    const orgMap = new Map<string, string>();
    const allOrgs = await prisma.organization.findMany();
    for (const org of allOrgs) {
      orgMap.set(org.orgSlug, org.id);
    }

    let jobCount = 0;
    let featuredCount = 0;
    let remoteCount = 0;
    let deadlineSoonCount = 0;
    let skippedCount = 0;

    const jobDataBatch: any[] = [];

    for (const job of jobs) {
      const resolvedSubValue = resolveSubcategoryValue(job.subcategoryValue, job.categoryValue);
      if (!resolvedSubValue) {
        console.warn(`   ⚠️  Subcategory "${job.subcategoryValue}" not found. Skipping job: ${job.title}`);
        skippedCount++;
        continue;
      }

      const subcategoryInfo = subcategoryMap.get(resolvedSubValue)!;
      const orgId = job.orgSlug ? orgMap.get(job.orgSlug) : null;
      const orgName = job.orgSlug
        ? organizations.find(o => o.orgSlug === job.orgSlug)?.name || ''
        : '';

      jobDataBatch.push({
        title: job.title,
        slug: `${slugify(job.title)}-${randInt(100, 999)}`,
        description: job.description,
        organizationId: orgId,
        subcategoryId: subcategoryInfo.id,
        categoryId: subcategoryInfo.categoryId,
        employmentType: job.employmentType as any,
        experienceLevel: job.experienceLevel as any,
        educationLevel: job.educationLevel as any,
        salaryDisclosure: job.salaryMin > 0 ? SalaryDisclosure.SHOW_RANGE : SalaryDisclosure.NOT_DISCLOSED,
        salaryMin: job.salaryMin > 0 ? job.salaryMin : null,
        salaryMax: job.salaryMax > 0 ? job.salaryMax : null,
        salaryCurrency: 'KES',
        locationCity: job.city,
        locationCounty: job.county,
        isRemote: job.isRemote,
        howToApply: job.howToApply,
        status: JobStatus.ACTIVE,
        featured: job.featured,
        datePosted: daysAgo(randInt(1, 15)),
        deadline: job.deadline,
        searchText: `${job.title} ${job.description} ${orgName} ${job.city} ${job.county}`,
        jobSource: 'MANUAL' as any,
      });
      jobCount++;
      if (job.featured) featuredCount++;
      if (job.isRemote) remoteCount++;
      if (job.deadline && job.deadline <= daysFromNow(14)) deadlineSoonCount++;
    }

    // Batch create jobs
    if (jobDataBatch.length > 0) {
      await prisma.job.createMany({ data: jobDataBatch });
    }

    console.log(`   ✅ Created ${jobCount} jobs (${featuredCount} featured, ${remoteCount} remote, ${deadlineSoonCount} closing soon).${skippedCount > 0 ? ` (${skippedCount} skipped due to missing subcategories.)` : ''}`);

    // -------------------------------------------------------
    // 4. SEED OPPORTUNITIES
    // -------------------------------------------------------
    console.log('\n🌟 Seeding opportunities...');

    // Batch create opportunities
    const oppFeaturedCount = opportunities.filter(o => o.featured).length;
    const oppResult = await prisma.opportunity.createMany({
      data: opportunities.map(opp => ({
        title: opp.title,
        slug: opp.slug,
        type: opp.type as any,
        providerName: opp.providerName,
        providerWebsite: opp.providerWebsite,
        description: opp.description,
        eligibilityCriteria: opp.eligibilityCriteria,
        benefits: opp.benefits,
        locationCity: opp.locationCity,
        locationCounty: opp.locationCounty,
        isRemote: opp.isRemote,
        isOnline: opp.isOnline,
        openToInternational: opp.openToInternational,
        fundingDisclosure: opp.fundingDisclosure as any,
        fundingAmount: opp.fundingAmount,
        fundingCurrency: opp.fundingCurrency,
        duration: opp.duration,
        applicationUrl: opp.applicationUrl,
        status: 'ACTIVE' as any,
        featured: opp.featured,
        datePosted: daysAgo(randInt(1, 30)),
        deadline: opp.deadline,
        targetDemographic: opp.type === OpportunityType.SCHOLARSHIP ? 'Students and young professionals' : 'Professionals and entrepreneurs',
      })),
    });

    console.log(`   ✅ Created ${oppResult.count} opportunities (${oppFeaturedCount} featured).`);

    // -------------------------------------------------------
    // SUMMARY
    // -------------------------------------------------------
    console.log('\n📊 Final Database Counts:');
    console.log(`   Categories:      ${await prisma.jobCategory.count()}`);
    console.log(`   Subcategories:   ${await prisma.jobSubcategory.count()}`);
    console.log(`   Organizations:   ${await prisma.organization.count()}`);
    console.log(`   Jobs:            ${await prisma.job.count()}`);
    console.log(`   Opportunities:   ${await prisma.opportunity.count()}`);
    console.log('\n✅ Seed completed successfully!');
  } catch (error) {
    console.error('❌ Seed failed:', error);
    throw error;
  } finally {
    await prisma.$disconnect();
  }
}
